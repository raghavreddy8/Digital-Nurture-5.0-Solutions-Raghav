public class TypeCasting{
    public static void main(String[] args) {
        double DoubleNumber=23.4567;
        int IntNumber=30;

        System.out.println("The double number "+DoubleNumber+"type casted to INT: "+(int)DoubleNumber);
        System.out.println("The integer number "+IntNumber+" type casted to Double: "+(double)IntNumber);
    }
}